<strong>Design Order Suggestion</strong>
<div class="spacer"></div>
TIP: It's a good idea to VIEW your project in a separate tab as you create it. Hit F5 to refresh the page and see your latest changes. <br />
<br />
1. Either download a pre-made skin in the THEMES section at the top or create your own, or just use the standard WEvolt theme already installed on your project. You can click CURRENT SKIN to the left to view your current settings.<br />
<br />
2. Once in CURRENT SKIN, go to TEMPLATE SETTINGS and edit your HEADER and FOOTER. If you upload an image for your header, make sure the height box is empty in this module so the area will be the height of your image. <br />
<br />
3. Under SITE APPEARANCE edit your WINDOW HEADER and your windows. Your windows are the frames in which all content on your site will appear in. Customize your own or choose from the pre-made ones we have created. <br />
<br />
4. Go to CONTENT. First edit your HOME section. Look at other projects for inspiration as to what you could do here. Like <span class="cms_links"><a href="http://www.wevolt.com/The_Path/">THE PATH</a></span> or <span class="cms_links"><a href="http://www.wevolt.com/Phimagic/">Phinmagic</a></span> <br />
<br />
5. Go to SETTINGS and check/edit your project's credits and info.<br />
<br />
6. Start creating pages under CONTENT. Note, if you don't want one of the suggested default pages to show, simply don't make a menu link for it. <br />
<br />
7. Once you have created all your pages, come back here and click on MENU on the left. You can use simple text boxes or upload images.
